def countApplesAndOranges(s, t, a, b, apples, oranges):
    apple_count = 0
    orange_count = 0

    for i in apples:
        pos_a = a+i
        if pos_a >= s and pos_a <= t:
            apple_count += 1
    
    for j in oranges:
        pos_o = b+j
        if pos_o >= s and pos_o <= t:
            orange_count += 1

    print(apple_count)
    print(orange_count)

